    private boolean cekTextBagianUpdateKosong() {
        boolean textBagianUpdateKosong = false;
        if (this.textIdMemberAkanDiupdate.getText().isEmpty()
                || this.textNamaMemberAkanDiupdate.getText().isEmpty()
                || this.textNoTeleponAkanDiupdate.getText().isEmpty()
                || this.textNamaMemberUpdate.getText().isEmpty()
                || this.textNoTeleponUpdate.getText().isEmpty()){
            textBagianUpdateKosong = true;}
        return textBagianUpdateKosong;
    }
    
    private boolean cekTextBagianHapusKosong() {
        return this.textIdMemberAkanDihapus.getText().isEmpty()
                ||this.textNamaMemberAkanDihapus.getText().isEmpty();
    }
    
    private boolean validasiInputanUpdateError() {
        String pesanErrorIdMember = "";

        boolean errorInputIdMember;
        try {
            int idMember = Integer.parseInt(this.textIdMemberAkanDiupdate.getText());
            errorInputIdMember = false;
        } catch (NumberFormatException error) {
            errorInputIdMember = true;
            pesanErrorIdMember = String.valueOf(error);
        }

        if (errorInputIdMember) {
            JOptionPane.showMessageDialog(null, "ID Member hanya menerima masukan berupa angka.\n"
                    + pesanErrorIdMember, "Peringatan", WARNING_MESSAGE);
        }
        return errorInputIdMember;
    }
    
