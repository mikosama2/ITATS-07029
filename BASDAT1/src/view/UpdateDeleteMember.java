    private void buttonCariMemberAkanDiupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariMemberAkanDiupdateActionPerformed
         boolean ketemu = false;
        if (this.textIdMemberAkanDiupdate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan isi ID pegawai terlebih dahulu", "Peringatan",
                    WARNING_MESSAGE);
        } else {
             try {
                 if (!this.aksesDataMember.cekDataMemberMasihKosong()) {
                     int idMember = Integer.parseInt(this.textIdMemberAkanDiupdate.getText());
                     ModelMember modelMember = this.aksesDataMember.cariDataMember(idMember);
                     if (modelMember != null) {
                         this.textNamaMemberAkanDiupdate.setText(modelMember.getNamaMember());
                         this.textNoTeleponAkanDiupdate.setText(String.valueOf(modelMember.getNoTelpMember()));
                         this.textNamaMemberAkanDiupdate.setEnabled(true);
                         this.textNoTeleponAkanDiupdate.setEnabled(true);
                         ketemu = true;
                     }
                 } else {
                     JOptionPane.showMessageDialog(null, "Data member masih kosong",
                             "Peringatan", WARNING_MESSAGE);
                 }
                 if (!ketemu && !this.aksesDataMember.cekDataMemberMasihKosong()) {
                     JOptionPane.showMessageDialog(null, "Member yang dicari tidak ditemukan, "
                             + "silahkan masukkan ID yang benar", "Peringatan", WARNING_MESSAGE);
                 }} catch (SQLException ex) {
                 Logger.getLogger(UpdateDeleteMember.class.getName()).log(Level.SEVERE, null, ex);
             }
        }                     
    }//GEN-LAST:event_buttonCariMemberAkanDiupdateActionPerformed

    private void buttonUpdateDataMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateDataMemberActionPerformed
        if (this.cekTextBagianUpdateKosong()) {
            JOptionPane.showMessageDialog(null, "Isi semua kolom isian terlebih dahulu", "Gagal Update",
                    WARNING_MESSAGE);
        } else {
            if (!this.validasiInputanUpdateError()) {
                int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin untuk "
                        + "mengupdate data member tersebut ?", "Konfirmasi", OK_CANCEL_OPTION,
                        QUESTION_MESSAGE);
                if (konfirmasi == 0) {
                    int idMember = Integer.parseInt(this.textIdMemberAkanDiupdate.getText());
                    String namaMember = this.textNamaMemberUpdate.getText();
                    int noTelpMember= Integer.parseInt(this.textNoTeleponUpdate.getText());
                    this.aksesDataMember.update(idMember, namaMember, noTelpMember);
                    JOptionPane.showMessageDialog(null, "Data pegawai tersebut berhasil diupdate");
                }
            }
        }
    }//GEN-LAST:event_buttonUpdateDataMemberActionPerformed

    private void buttonCariMemberAkanDihapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariMemberAkanDihapusActionPerformed
        boolean ketemu = false;
        if (this.textIdMemberAkanDihapus.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan isi ID pegawai terlebih dahulu", "Peringatan",
                    WARNING_MESSAGE);
        } else {
            try {
                if (!this.aksesDataMember.cekDataMemberMasihKosong()) {
                    int idMember = Integer.parseInt(this.textIdMemberAkanDihapus.getText());
                    ModelMember modelMember = this.aksesDataMember.cariDataMember(idMember);
                    if (modelMember != null) {
                        this.textNamaMemberAkanDihapus.setText(modelMember.getNamaMember());
                        this.textNoTeleponAkanDihapus.setText(String.valueOf(modelMember.getNoTelpMember()));
                        this.textNamaMemberAkanDihapus.setEnabled(true);
                        this.textNoTeleponAkanDihapus.setEnabled(true);
                        ketemu = true;
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Data pegawai masih kosong",
                            "Peringatan", WARNING_MESSAGE);
                }
                if (!ketemu && !this.aksesDataMember.cekDataMemberMasihKosong()) {
                    JOptionPane.showMessageDialog(null, "Member yang dicari tidak ditemukan, "
                            + "silahkan masukkan ID yang benar", "Peringatan", WARNING_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(UpdateDeleteMember.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonCariMemberAkanDihapusActionPerformed

    private void buttonHapusMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusMemberActionPerformed
        if (this.cekTextBagianHapusKosong()) {
            JOptionPane.showMessageDialog(null, "Isi semua kolom isian terlebih dahulu", "Gagal Hapus",
                    WARNING_MESSAGE);
        } else {
            if (!this.validasiInputanHapusError()) {
                int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin untuk menghapus "
                        + "data member tersebut ?", "Konfirmasi",
                        OK_CANCEL_OPTION, QUESTION_MESSAGE);
                if (konfirmasi == 0) {
                    int idMember = Integer.parseInt(this.textIdMemberAkanDihapus.getText());
                    this.aksesDataMember.delete(idMember);
                    JOptionPane.showMessageDialog(null, "Data member tersebut berhasil dihapus");
                }
            }
        }
    }//GEN-LAST:event_buttonHapusMemberActionPerformed

